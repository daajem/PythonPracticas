# 1 Importo solo la funcion sqrt
from math import sqrt

# Y calculo la raiz cuadrado de un numero
numero = 25
raiz_cuadrada = sqrt(numero)
print("La raíz cuadrada de", numero, "es:", raiz_cuadrada)

# 2 Importo math con un alias
import math as raizCuadrado

# Utilizo la función sqrt con el alias para calcular la raíz cuadrada de un número
numero = 36
raiz_cuadrada = raizCuadrado.sqrt(numero)
print("La raíz cuadrada de", numero, "es:", raiz_cuadrada)

